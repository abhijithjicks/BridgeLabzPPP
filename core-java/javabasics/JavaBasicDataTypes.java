public class JavaBasicDataTypes {
    int a = 111;
    double b = 26.5;
    String message = "Welcome to Java Basics";
    boolean isJavaFun = true;

    public static void main(String[] args) {
        JavaBasicDataTypes example = new JavaBasicDataTypes();
        System.out.println("Integer: " + example.a);
        System.out.println("Double: " + example.b);
        System.out.println("String: " + example.message);
        System.out.println("Boolean: " + example.isJavaFun);
    }
}
